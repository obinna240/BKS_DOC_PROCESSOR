package org.pcg.bucks.careact.documentUtils;

public class TableContent 
{
	private String tableId;
	private String rowValue;
	private String tableDefValue;
	private String fullTableContent;
	
	public String getTableId() {
		return tableId;
	}
	public void setTableId(String tableId) {
		this.tableId = tableId;
	}
	public String getRowValue() {
		return rowValue;
	}
	public void setRowValue(String rowValue) {
		this.rowValue = rowValue;
	}
	
	public String getFullTableContent() {
		return fullTableContent;
	}
	public void setFullTableContent(String fullTableContent) {
		this.fullTableContent = fullTableContent;
	}
	public String getTableDefValue() {
		return tableDefValue;
	}
	public void setTableDefValue(String tableDefValue) {
		this.tableDefValue = tableDefValue;
	}
	
	
	
}
